public class GradeWhileLoop {
    public static void main(String[] args) {
        int i = 4;
        while (i > 0) {
            System.out.print(i);
            i--;
        }

        int x = 0;
        while (x < 10) {
            System.out.println(x);
            x++;
        }

        int k;
        for (k = 0; k < 10; k++) {
            System.out.print(i);
        }

        int j;
        for (j = 0; j < 10; j++) {
            System.out.print(j);
        }
    }
}

